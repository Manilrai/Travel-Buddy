/**
 * Get all services for a given type
 */
export const getServicesByType = (type) => {
    switch (type) {
        case 'bus': return busServices;
        case 'hotel': return hotelServices;
        case 'trek': return trekServices;
        default: return [];
    }
};

/**
 * Get a single service by type and id
 */
export const getServiceById = (type, id) => {
    const services = getServicesByType(type);
    return services.find((s) => s.id === id) || null;
};
